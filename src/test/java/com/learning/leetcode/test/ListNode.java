package com.learning.leetcode.test;

import java.io.Serializable;

public class ListNode implements Serializable {
    public int val;
    public ListNode next;

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
